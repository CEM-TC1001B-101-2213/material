import turtle as t

def Aram():
    t.left(90)
    t.fd(100)
    t.right(90)
    t.fd(50)
    t.right(90)
    t.fd(30)
    t.right(90)
    t.fd(50)
    t.backward(50)
    t.left(90)
    t.fd(70)
    t.left(90)
    
    t.penup()
    t.fd(30)
    t.pendown()
    
    t.left(90)
    t.fd(100)
    
Aram()
t.mainloop()